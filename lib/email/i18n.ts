import type { Locale } from '@/i18n/config'

export type EmailLocale = 'de' | 'en' | 'uk'

export function toEmailLocale(input: string | null | undefined): EmailLocale {
  if (input === 'en') return 'en'
  if (input === 'uk') return 'uk'
  return 'de'
}

